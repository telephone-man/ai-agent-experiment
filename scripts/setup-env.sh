#!/usr/bin/env sh
set -eu

usage() {
  cat <<'USAGE'
Usage: scripts/setup-env.sh [--force]

Creates local environment files from the committed root template:
  .env       copied from .env.example
  conf/env   generated from .env.example with shell-safe quoting

Existing files are left untouched unless --force is passed.
USAGE
}

force=0
case "${1:-}" in
  "")
    ;;
  --force)
    force=1
    ;;
  -h|--help)
    usage
    exit 0
    ;;
  *)
    usage >&2
    exit 2
    ;;
esac

root_dir=${SETUP_ENV_ROOT_DIR:-$(CDPATH= cd -- "$(dirname -- "$0")/.." && pwd)}

validate_env_key() {
  key=$1
  if ! printf '%s\n' "$key" | grep -Eq '^[A-Za-z_][A-Za-z0-9_]*$'; then
    printf 'Invalid env key in .env.example: %s\n' "$key" >&2
    exit 1
  fi
}

shell_quote_value() {
  value=$1
  printf "'%s'" "$(printf '%s' "$value" | sed "s/'/'\\\\''/g")"
}

warn_missing_template_keys() {
  template=$1
  target=$2
  missing_keys=""

  while IFS= read -r line || [ -n "$line" ]; do
    case "$line" in
      ""|\#*)
        continue
        ;;
      *=*)
        key=${line%%=*}
        validate_env_key "$key"
        if ! grep -q "^${key}=" "$target" 2>/dev/null; then
          missing_keys="${missing_keys} ${key}"
        fi
        ;;
    esac
  done < "$template"

  if [ -n "$missing_keys" ]; then
    printf 'Warning: existing %s is missing .env.example keys:%s\n' "${target#$root_dir/}" "$missing_keys" >&2
    printf 'Run ./setup.sh --force to regenerate local env files from the current template.\n' >&2
  fi
}

copy_template() {
  template=$1
  target=$2

  if [ ! -f "$template" ]; then
    printf 'Missing template: %s\n' "$template" >&2
    exit 1
  fi

  if [ -f "$target" ] && [ "$force" -eq 0 ]; then
    printf 'Keeping existing %s\n' "${target#$root_dir/}"
    warn_missing_template_keys "$template" "$target"
    return
  fi

  cp "$template" "$target"
  printf 'Created %s from %s\n' "${target#$root_dir/}" "${template#$root_dir/}"
}

generate_shell_env() {
  template=$1
  target=$2
  tmp="${target}.tmp.$$"

  if [ ! -f "$template" ]; then
    printf 'Missing template: %s\n' "$template" >&2
    exit 1
  fi

  if [ -f "$target" ] && [ "$force" -eq 0 ]; then
    printf 'Keeping existing %s\n' "${target#$root_dir/}"
    warn_missing_template_keys "$template" "$target"
    return
  fi

  {
    printf '# Generated by scripts/setup-env.sh from .env.example.\n'
    printf '# Shell-sourceable runtime baseline for telecom entrypoints.\n'
    printf '# Edit .env for reviewer-facing Docker Compose overrides.\n'
    printf '# Regenerate with ./setup.sh --force after .env.example changes.\n'
    printf '\n'
    while IFS= read -r line || [ -n "$line" ]; do
      case "$line" in
        ""|\#*)
          printf '%s\n' "$line"
          ;;
        *=*)
          key=${line%%=*}
          value=${line#*=}
          validate_env_key "$key"
          printf '%s=%s\n' "$key" "$(shell_quote_value "$value")"
          ;;
        *)
          printf 'Unsupported line in .env.example: %s\n' "$line" >&2
          exit 1
          ;;
      esac
    done < "$template"
  } > "$tmp"

  mv "$tmp" "$target"
  printf 'Created %s from %s with shell-safe quoting\n' "${target#$root_dir/}" "${template#$root_dir/}"
}

set_env_value() {
  file=$1
  key=$2
  value=$3
  tmp="${file}.tmp.$$"
  escaped_value=$(printf '%s' "$value" | sed 's/[\/&]/\\&/g')

  if grep -q "^${key}=" "$file"; then
    sed "s/^${key}=.*/${key}=${escaped_value}/" "$file" > "$tmp"
  else
    cp "$file" "$tmp"
    printf '%s=%s\n' "$key" "$value" >> "$tmp"
  fi

  mv "$tmp" "$file"
}

set_shell_env_value() {
  file=$1
  key=$2
  value=$3
  tmp="${file}.tmp.$$"
  found=0
  quoted_value=$(shell_quote_value "$value")

  while IFS= read -r line || [ -n "$line" ]; do
    case "$line" in
      "${key}="*)
        printf '%s=%s\n' "$key" "$quoted_value"
        found=1
        ;;
      *)
        printf '%s\n' "$line"
        ;;
    esac
  done < "$file" > "$tmp"

  if [ "$found" -eq 0 ]; then
    printf '%s=%s\n' "$key" "$quoted_value" >> "$tmp"
  fi

  mv "$tmp" "$file"
}

get_env_value() {
  file=$1
  key=$2
  grep "^${key}=" "$file" 2>/dev/null | tail -n 1 | sed "s/^${key}=//"
}

env_value_is_empty() {
  case "$1" in
    ""|"''"|'""') return 0 ;;
    *) return 1 ;;
  esac
}

generate_secret() {
  if command -v openssl >/dev/null 2>&1; then
    openssl rand -hex 18
    return
  fi
  if command -v python3 >/dev/null 2>&1; then
    python3 -c 'import secrets; print(secrets.token_hex(18))'
    return
  fi
  date +%s | awk '{print "local" $1 "change"}'
}

ensure_secret_value() {
  file=$1
  key=$2
  mode=$3
  current=$(get_env_value "$file" "$key")
  if env_value_is_empty "$current"; then
    secret=$(generate_secret)
    if [ "$mode" = "shell" ]; then
      set_shell_env_value "$file" "$key" "$secret"
    else
      set_env_value "$file" "$key" "$secret"
    fi
  fi
}

populate_local_secrets() {
  file=$1
  mode=$2
  ensure_secret_value "$file" "KAMAILIO_DB_SUPERUSER_PASS" "$mode"
  ensure_secret_value "$file" "KAMAILIO_DB_RW_PASS" "$mode"
  ensure_secret_value "$file" "KAMAILIO_DB_RO_PASS" "$mode"
  ensure_secret_value "$file" "FREESWITCH_ESL_PASSWORD" "$mode"
}

dotenv="$root_dir/.env"
confenv="$root_dir/conf/env"

copy_template "$root_dir/.env.example" "$dotenv"
generate_shell_env "$root_dir/.env.example" "$confenv"
populate_local_secrets "$dotenv" "dotenv"
populate_local_secrets "$confenv" "shell"

if [ -t 0 ]; then
  printf '\nOPENAI_API_KEY (leave blank to fill in .env later): '
  read -r openai_api_key
  if [ -n "$openai_api_key" ]; then
    set_env_value "$dotenv" "OPENAI_API_KEY" "$openai_api_key"
  fi

  printf 'Enable offline demo fallback instead of live provider mode? [y/N] '
  read -r offline_mode
  case "$offline_mode" in
    y|Y|yes|YES)
      set_env_value "$dotenv" "AI_OFFLINE_FALLBACK" "1"
      printf 'Enabled offline demo fallback.\n'
      ;;
    *)
      printf 'Keeping live provider defaults.\n'
      ;;
  esac
fi

cat <<EOF

Environment setup complete.

Review these local-only files before starting the stack:
  .env      - reviewer-facing Docker Compose overrides and API keys
  conf/env  - shell-safe runtime baseline and local telecom secrets

Live AI mode is the default. Set OPENAI_API_KEY in .env before starting the
stack. For offline demos, set AI_OFFLINE_FALLBACK=1 in .env. The optional README
weather walkthrough also needs LLM_ENABLE_MOCK_WEATHER_TOOL=1 in .env.

If setup keeps an existing env file and warns about missing keys, rerun:
  ./setup.sh --force
EOF
